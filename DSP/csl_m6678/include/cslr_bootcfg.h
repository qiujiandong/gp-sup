    //0x140
    volatile Uint8 RSVD4[4];
    volatile Uint32 RESET_STAT;
    volatile Uint32 LRSTNMIPINSTAT;
    volatile Uint32 DEVCFG;
    //0x150
    volatile Uint32 PWRSTATECTL;
    volatile Uint8 RSVD5[8];
    volatile Uint32 PCIE_SERDES_STS;
    //0x160
    volatile Uint8 RSVD6[160];
    //0x200
    volatile Uint32 NMIGR[8];
    //0x220
    volatile Uint8 RSVD7[32];
    //0x240
    volatile Uint32 IPCGR[8];
    //0x260
    volatile Uint8 RSVD8[28];
    //0x27C
    volatile Uint32 IPCGRH;
    //0x280
    volatile Uint32 IPCAR[8];
    //0x2A0
    volatile Uint8 RSVD9[28];
    //0x2BC
    volatile Uint32 IPCARH;
    //0x2C0
    volatile Uint8 RSVD10[64];
    //0x300
    volatile Uint32 TINPSEL;
    volatile Uint32 TOUTSEL;
    volatile Uint32 RSTMUX[8];
    //0x328
    volatile Uint32 MAINPLLCTL0;
    volatile Uint32 MAINPLLCTL1;
    volatile Uint32 DDR3PLLCTL0;
    volatile Uint32 DDR3PLLCTL1;
    volatile Uint32 PASSPLLCTL0;
    volatile Uint32 PASSPLLCTL1;
    //0x340
    volatile Uint32 GMAC_SERDES_CFGPLL;
    volatile Uint8 RSVD11[20];
    //0x358
    volatile Uint32 PCIE_SERDES_CFGPLL;
    volatile Uint8 RSVD12[20];
    //0x360
